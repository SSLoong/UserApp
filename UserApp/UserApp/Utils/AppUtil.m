+ (BOOL)isNetworkExist{
    
    Reachability *reachability = [Reachability reachabilityWithHostName:SITE_SERVER];
    
    return reachability.currentReachabilityStatus > 0;
}

+ (MBProgressHUD *)createHUD{
    
    UIWindow *window = [[UIApplication sharedApplication].windows lastObject];
    MBProgressHUD *HUD = [[MBProgressHUD alloc] initWithWindow:window];
    HUD.userInteractionEnabled = NO;
    HUD.detailsLabelFont = [UIFont boldSystemFontOfSize:16];
    [window addSubview:HUD];
    [HUD show:YES];
    HUD.removeFromSuperViewOnHide = YES;
    return HUD;
}


+(NSString *)getStartTime{

    NSString *endTime = [AppUtil getEndTime];
    
    return [NSString stringWithFormat:@"%@01",[endTime substringToIndex:8]];
    
}

+(NSString *)getEndTime{

    NSDate *currentDate = [NSDate date];//获取当前时间，日期
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];
    [dateFormatter setDateFormat:@"YYYY-MM-dd"];
    NSString *dateString = [dateFormatter stringFromDate:currentDate];
    return dateString;
}


