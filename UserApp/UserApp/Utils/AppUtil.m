+ (NSString*)UUIDString{

    
    NSString * strUUID = (NSString *)[KeyChainStore load:KEY_USERNAME_PASSWORD];
    
    //首次执行该方法时，uuid为空
    if ([strUUID isEqualToString:@""] || !strUUID)
    {
        //生成一个uuid的方法
        CFUUIDRef uuidRef = CFUUIDCreate(kCFAllocatorDefault);
        
        strUUID = (NSString *)CFBridgingRelease(CFUUIDCreateString (kCFAllocatorDefault,uuidRef));
        
        //将该uuid保存到keychain
        [KeyChainStore save:KEY_USERNAME_PASSWORD data:strUUID];
        
    }
    
    
    return  strUUID;
}


+(NSString *)getCityCode:(NSString *)cityName{

    NSString *path=[[NSBundle mainBundle] pathForResource:@"citycode" ofType:@"plist"];
    
    NSArray *dicArray=[[NSArray alloc]initWithContentsOfFile:path];
    
    NSString *cityCode;
    
    for (NSDictionary *dic in dicArray) {
    
        if ([dic[@"cityName"] isEqualToString:cityName]) {
            
            cityCode = dic[@"cityCode"];

            break;
        }
    }

    return cityCode;
}


